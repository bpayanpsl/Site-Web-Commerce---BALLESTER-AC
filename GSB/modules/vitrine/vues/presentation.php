						Le laboratoire Galaxy Swiss Bourdin (GSB) est issu de la <mark>fusion</mark> entre le géant<mark> américain Galaxy</mark> (spécialisé dans le <i>secteur des maladies virale</i>  dont le SIDA et les hépatites) et le conglomérat <mark> européen Swiss Bourdin</mark>  (<i>travaillant sur des médicaments plus conventionnels</i>), lui même déjà union de trois petits laboratoires .
						En <i>2009</i>, les deux géants pharmaceutiques ont <mark> uni leurs forces</mark>  pour créer un leader de ce secteur industriel. L'entité Galaxy Swiss Bourdin Europe a établi son <i>siège administratif à Paris. </i>
						<i>Le siège social de la multinationale est situé à Philadelphie, Pennsylvanie, aux Etats-Unis.</i>
