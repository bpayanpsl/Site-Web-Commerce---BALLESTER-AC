					<div><h5>Les visiteurs</h5>
							La force commerciale d'un laboratoire pharmaceutique est <mark>assurée par un travail de conseil et d'information auprès des prescripteurs</mark>. <mark>Les visiteurs médicaux</mark> (ou délégués) démarchent les <i>médecins, pharmaciens, infirmières et autres métiers de santé</i> susceptibles de prescrire aux patients les produits du laboratoire.
							<br><u><b>L'objectif</b></u> d'une visite est <mark>d'actualiser</mark> et <mark>rafraîchir la connaissance des professionnels de santé</mark> sur les produits de l'entreprise. Les visiteurs ne font pas de vente, mais leurs interventions ont un impact certain sur la prescription de la pharmacopée du laboratoire.
							<br><br>Pour donner une organisation commune aux délégués médicaux, l'entreprise a adopté l'organisation de la flotte de visiteurs existant chez Galaxy, selon un système hiérarchique par région et, à un niveau supérieur, par secteur géographique (Sud, Nord, paris-Centre, etc).
							Il n'y a pas eu d'harmonisation de la relation entre les personnels de terrain (Visiteurs et Délégués régionaux) et les responsables de secteur. Les habitudes en cours avant la fusion ont été adaptées sans que soient données des directives au niveau local.

