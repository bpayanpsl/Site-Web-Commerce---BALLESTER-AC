						<h5>Les visiteurs et les autres services</h5>
							Les déplacements et actions de terrain menées par les visiteurs engendrent des frais qui doivent être pris en charge par la comptabilité. On cherche à agir au plus juste de manière à limiter les excès sans pour autant diminuer les frais de représentation qui font partie de l'image de marque d'un laboratoire.
							<br>
							<img class="gsb" src="./images/3.jpg" width="400px">
							<br>
							<mark>Chez Galaxy</mark>, le principe d'engagement des frais est celui de la carte bancaire au nom de l'entreprise. Chez Swiss-Bourdin, une gestion forfaitaire des principaux frais permet de limiter les justificatifs. Pour tout le reste, le remboursement est fait après retour des pièces justificatives.
							Une gestion unique de ces frais et remboursement pour l'ensemble de la flotte visite est souhaitée.
							<br><br>Les visiteurs récupèrent une information directe sur le terrain. Ceci concerne aussi bien le niveau de la confiance qu'inspire le laboratoire que la lisibilité des notices d'utilisation des médicaments ou encore les éventuels problèmes rencontrés lors de leur utilisation, etc..
							Ces informations ne sont actuellement pas systématiquement remontées au siège, ou elles le sont dans des délais jugés trop longs. Le service rédaction qui produit les notices souhaite avoir des remontées plus régulières et directes. Ceci permettra également au service labo-recherche d’engager des évaluations complémentaires. 
							<br><br><mark>Le turn-over des visiteurs</mark> est de plus en plus important. Pour un délégué régional et plus encore un responsable de secteur, le suivi des équipes devient une véritable activité : obtenir les coordonnées auprès des services RH lors de l'arrivée d'un nouveau personnel, réaliser un suivi personnalisé et former les recrues, etc. 
							Un accès plus direct aux données de personnel est nécessaire. 

